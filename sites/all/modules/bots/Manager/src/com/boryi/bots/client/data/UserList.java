package com.boryi.bots.client.data;

import java.util.ArrayList;

/**
 * Data bundle for users
 * 
 * @author Gang Huang Ph.D.
 * @version v1.0.0
 * @since 2009-09-13
 */
public class UserList extends TableList
{
  public UserList()
  {
    headers = new ArrayList<String>(13);
    headers.add("Select"); // whether the user account is selected
    headers.add("Group"); // name of the user group
    headers.add("Id"); // id of the user
    headers.add("Name"); // name of the user account
    headers.add("Password"); // password of the user account
    headers.add("Enabled"); // whether the user account is enabled
    headers.add("Nickname"); // nickname of the user
    headers.add("Created"); // when the user account was created
    headers.add("Upaded"); // when the user account was updated last
    headers.add("First"); // when the user first logged in
    headers.add("Last"); // when the user last logged in
    headers.add("LastIp"); // ip of the user last logged in
    headers.add("Logins"); // total time that the user logged in
    headers.add("Description"); // description of the user account

    sort = "id"; // sort by user id
    // name: sort by name 
    // enabled: sort by enabled
    // created: sort by created
    // updated:  sort by last updated
  }
}
